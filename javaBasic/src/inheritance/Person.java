package inheritance;

public class Person {
	String name;int id,age,contact;


	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}


	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}


	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}


	public int getContact() {
		return contact;
	}
	public void setContact(int contact) {
		this.contact = contact;
	}
	

}
