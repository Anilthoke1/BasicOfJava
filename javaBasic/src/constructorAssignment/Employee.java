package constructorAssignment;

public class Employee {
	private int id; 
	private String name, salary;
	
	
	
	void setId(int id) {
		this.id=id;
	}
	int getId() {
		return id;
	}
	void setName(String name) {
		this.name=name;
	}
	String getName() {
		return name;
	}
	void setSalary(String salary) {
		this.salary=salary;
	}
	String getSalary() {
		return salary;
	}
	
	
	

	
}
