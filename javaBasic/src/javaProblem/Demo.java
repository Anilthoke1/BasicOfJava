package javaProblem;

public class Demo {
	
	public static void main(String[] args) {
	
	for(int i=0;i<6;i++) {
	for(int j=0;j<=i;j++) {
		System.out.print(j+" ");
	}
	
		System.out.println();

	}
	for(int f=4;f>=0;f--) {
		for(int h=0;h<=f;h++) {
			System.out.print(h+" ");
		}
		System.out.println();
	}
	
	
	
	
	}
	
}
