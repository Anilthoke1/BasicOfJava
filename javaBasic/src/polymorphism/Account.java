package polymorphism;

class Account {
	private int id,number ,balance;

	void setId(int id) {
		this.id=id;
	}
	int getId() {
		return id;
	}
	void setNumber(int number ) {
		this.number=number;
	}
	int getNumber() {
		return number;
	}
	void setBalance(int balance ) {
		this.balance=balance;
	}
	int getBalance() {
		return balance;
	}
	
}
